#include "mathematics.h"

Mathematics::Mathematics() {}

double Mathematics::add(double a, double b) {
	return a + b;
}

double Mathematics::sub(double a, double b) {
	return a - b;
}

double Mathematics::mul(double a, double b) {
	return a * b;
}

double Mathematics::div(double a, double b) {
	return a / b;
}
